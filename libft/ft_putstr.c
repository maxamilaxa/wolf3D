#include "libft.h"

void				ft_putstr(char const *s)
{
	char	*str;
	int		i;

	str = (char*)s;
	i = 0;
	if (s)
	{
		while (str[i] != '\0')
		{
			ft_putchar(str[i]);
			i++;
		}
	}
}
